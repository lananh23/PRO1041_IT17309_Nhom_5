    void doiPassword() {

        String passCu = new String(txtPasswordCu.getPassword());
        String passMoi = new String(txtPasswordMoi.getPassword());
        String nhapLaiPassword = new String(txtNhapLaiPassword.getPassword());

        if (dangNhapService.KT_DoiMK(passCu, passMoi, nhapLaiPassword)) {
            dangNhapService.KT_SuaMK(passMoi, repositories.TenUser);
        }

    }

